def get_index_by_name(lst, target_name):
    return next((i for i, item in enumerate(lst) if item.name == target_name), -1)

def randomize_shop(world, mapping, locs):

    for index in range(len(mapping)):
        data = locs[index]
        data.locData.region = locs[mapping[index]].locData.region
        global location_dict
        location_dict[data.name] = data.locData


def swap_within_n(world, lst, target, n, invalid_indexes):
    if target not in lst:
        return invalid_indexes  # Return unchanged invalid list if target not found

    index = lst.index(target)  # Find the index of the target

    # Generate a valid swap index (between 0 and n, but not in invalid_indexes)
    valid_indexes = [i for i in range(min(n + 1, len(lst))) if i not in invalid_indexes]

    if not valid_indexes:
        return invalid_indexes  # No valid swaps available

    swap_index = world.random.choice(valid_indexes)  # Pick a valid index

    # Swap the target element with the chosen index
    lst[index], lst[swap_index] = lst[swap_index], lst[index]

    # Add the new index to the invalid list
    invalid_indexes.append(swap_index)

    return invalid_indexes

def generate_card(name, index, border, foil, expansion):
    loc_id = 0x1F290000 | (int(expansion) << 12) | (int(border) << 8) | (foil << 7) | (index + 1)
    global location_dict
    location_dict[f"{name} {border.name} {'Foil' if foil else 'NonFoil'} {expansion.name}"] = LocData(loc_id, "")


def generate_locations(world):
    start_id = 0x1F2800F1
    current_loc = 0

    global location_dict
    global pg1_mapping
    global pg2_mapping
    global pg3_mapping
    global tt_mapping

    invalid_indexes = []
    world.random.shuffle(pg1_mapping)
    swap_within_n(world, pg1_mapping, get_index_by_name(hardcoded_pg1_locs, "Basic Card Pack (32)"), 12, invalid_indexes)

    randomize_shop(world, pg1_mapping, hardcoded_pg1_locs)

    invalid_indexes = []
    world.random.shuffle(pg1_mapping)
    swap_within_n(world, pg1_mapping, get_index_by_name(hardcoded_pg1_locs, "Basic Card Pack (32)"), 18, invalid_indexes)
    randomize_shop(world, pg2_mapping, hardcoded_pg2_locs)
    randomize_shop(world, pg3_mapping, hardcoded_pg3_locs)
    randomize_shop(world, tt_mapping, hardcoded_tt_locs)

    location_dict += hardcoded_locs
    current_loc += (len(hardcoded_pg1_locs) + len(hardcoded_pg2_locs) + len(hardcoded_pg3_locs) + len(hardcoded_tt_locs) + len(hardcoded_locs))

    for level in range(2, 116):
        loc_id = start_id + current_loc + level - 2
        # WHY     (XX0-XX4 but 1 - 4)
        start_level = max(1, level - (level % 5))
        end_level = (level - (level % 5)) + 4
        # WHY     (XX0-XX4 but 110 - 115)
        if end_level == 114:
            end_level = 115
        location_dict[f"Level {level}"] = LocData(loc_id, f"Level {start_level}-{end_level}")
        if world.options.goal.value == 1 and world.options.level_goal.value == level:
            location_dict[f"Level {level}"] = LocData(None, f"Level {start_level}-{end_level}")
        current_loc += 1

    for index, data in enumerate(card_rarity):
        data = cast(MonsterData, data)
        for border in Border:
            if world.options.card_sanity.value >= int(data.rarity):
                generate_card(data.name, index, border, 0, Expansion.Tetramon)
                generate_card(data.name, index, border, 1, Expansion.Tetramon)
            if world.options.card_sanity.value >= int(data.rarity) + 4:
                generate_card(data.name, index, border, 0, Expansion.Destiny)
                generate_card(data.name, index, border, 1, Expansion.Destiny)
    return location_dict


def create_locations(world, region):
    create_locations_from_dict(world, location_dict, region)


def create_locations_from_dict(world, loc_dict, region):
    for (key, data) in loc_dict.items():
        create_location(world, region, key, data)


def create_location(world, region, name: str, code: int):
    location = Location(world.player, name, code, region)
    region.locations.append(location)


